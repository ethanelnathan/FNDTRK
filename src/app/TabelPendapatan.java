    public void plusRow(User user){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rowData[] = new Object[3];
        for(int i = 0; i < user.getArrPend().size(); i++){
            rowData[0] = user.getArrPend().get(i).desc;
            rowData[1] = user.getArrPend().get(i).amount;
            rowData[2] = user.getArrPend().get(i).date;
            model.addRow(rowData);
        }
    }

